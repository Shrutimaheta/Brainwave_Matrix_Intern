    private void showHistory() {
    StringBuilder sb = new StringBuilder();
    for (String entry : Bank.history) {
        sb.append(entry).append("\n");
    }
    if (sb.length() == 0) {
        sb.append("No transactions yet.");
    }
    jTextArea1.setText(sb.toString());
}
